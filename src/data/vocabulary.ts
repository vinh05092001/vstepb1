export const getRandomVocabulary = (count: number = 5, filterTopic?: string, filterLevel?: string) => {
  let pool = [...INITIAL_VOCABULARY];
  if (filterTopic) pool = pool.filter(v => v.topic === filterTopic);
  if (filterLevel) pool = pool.filter(v => v.level === filterLevel);
  return pool.sort(() => 0.5 - Math.random()).slice(0, count);
};
